#!/usr/bin/env node
import { readFileSync, writeFileSync, readdirSync } from 'fs';
import { join, dirname } from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);

const CONTENT_DIR = join(__dirname, '../content/projects');
const OUTPUT_FILE = join(__dirname, '../generated/generatedProjects.ts');

console.log('🔧 Generating generatedProjects.ts...\n');

// Required normalized fields for validation
const REQUIRED_FIELDS = ['id', 'title', 'category', 'date', 'description', 'image'];

// Read all JSON files from content/projects/
let files;
try {
    files = readdirSync(CONTENT_DIR).filter(f => f.endsWith('.json'));
} catch (err) {
    console.error(`❌ Error reading directory: ${CONTENT_DIR}`);
    console.error(err.message);
    process.exit(1);
}

console.log(`Found ${files.length} project file(s):`);
files.forEach(f => console.log(`  - ${f}`));
console.log('');

// Load and validate all projects
const projects = [];
let errorCount = 0;

for (const filename of files) {
    const filepath = join(CONTENT_DIR, filename);
    try {
        const content = readFileSync(filepath, 'utf-8');
        const project = JSON.parse(content);
        
        // Validate required fields
        const missingFields = REQUIRED_FIELDS.filter(field => !project[field]);
        if (missingFields.length > 0) {
            console.error(`❌ Missing required fields in ${filename}:`);
            console.error(`   ${missingFields.join(', ')}`);
            errorCount++;
            continue;
        }
        
        projects.push(project);
        console.log(`✅ Validated: ${filename} (id: ${project.id})`);
    } catch (err) {
        console.error(`❌ Invalid JSON in ${filename}:`);
        console.error(`   ${err.message}`);
        errorCount++;
    }
}

console.log('');

if (errorCount > 0) {
    console.error(`❌ Failed to process ${errorCount} file(s). Aborting.`);
    process.exit(1);
}

// Generate TypeScript file
const tsContent = `// Auto-generated by scripts/generate-projects.mjs
// DO NOT EDIT MANUALLY - Edit source files in content/projects/ instead

export const generatedProjects = ${JSON.stringify(projects, null, 2)} as const;

export type Project = typeof generatedProjects[number];
`;

try {
    writeFileSync(OUTPUT_FILE, tsContent, 'utf-8');
    console.log(`✅ Generated: ${OUTPUT_FILE}`);
    console.log(`   Projects: ${projects.length}`);
    console.log(`   Size: ${(tsContent.length / 1024).toFixed(2)} KB`);
} catch (err) {
    console.error(`❌ Error writing output file:`);
    console.error(`   ${err.message}`);
    process.exit(1);
}

console.log('\n✨ Generation complete!');
console.log('\nNext steps:');
console.log('  1. Import in component: import { generatedProjects } from "./generated/generatedProjects"');
console.log('  2. Apply adapter: const projects = generatedProjects.map(adaptProject)');
console.log('  3. Commit and push to trigger GitHub Link sync');
